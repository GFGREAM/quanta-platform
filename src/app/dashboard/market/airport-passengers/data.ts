// Airport passenger data + parser. RAW_DATA holds the source dump (one row
// per line, fields separated by 2+ spaces or tabs). parseRaw() converts it to
// structured rows once at module load. Replace RAW_DATA with a fetch when the
// upstream table is wired up.

export interface PassengerRow {
  destination: string;
  year: number;
  month: number; // 1-12
  international: number;
  domestic: number;
}

export type Destination = string;

export const MONTHS = [
  'Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun',
  'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec',
] as const;

const MONTH_NAMES: Record<string, number> = {
  January: 1, February: 2, March: 3, April: 4, May: 5, June: 6,
  July: 7, August: 8, September: 9, October: 10, November: 11, December: 12,
};

function parseValue(s: string | undefined): number | null {
  if (!s) return null;
  const t = s.trim();
  if (!t) return null;
  if (t === '-') return 0;
  const n = parseInt(t.replace(/,/g, ''), 10);
  return Number.isFinite(n) ? n : null;
}

// Parses the heredoc dump. Each row: `YEAR MONTH DD/MM/YYYY MARKET DOM INTL`,
// where DOM/INTL may be empty, "-", or a comma-formatted integer. When only
// one value is present, it's assumed to be INTL (matches international-only
// airports like Aruba / Nassau / Kingston in the source data).
function parseRaw(raw: string): PassengerRow[] {
  const rows: PassengerRow[] = [];
  const seen = new Set<string>();
  for (const line of raw.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed) continue;
    const m = trimmed.match(/^(\d{4})\s+(\w+)\s+\d{1,2}\/\d{1,2}\/\d{4}\s+(.+)$/);
    if (!m) continue;
    const [, yearStr, monthName, rest] = m;
    const year = parseInt(yearStr, 10);
    const month = MONTH_NAMES[monthName];
    if (!month) continue;

    const parts = rest.split(/\s{2,}|\t+/).map((p) => p.trim()).filter((p) => p !== '');
    if (parts.length === 0) continue;

    const market = parts[0];
    let dom: number | null = null;
    let intl: number | null = null;
    if (parts.length === 2) {
      intl = parseValue(parts[1]);
    } else if (parts.length >= 3) {
      dom = parseValue(parts[1]);
      intl = parseValue(parts[2]);
    }
    if (dom === null && intl === null) continue;

    const key = `${market}|${year}|${month}`;
    if (seen.has(key)) continue;
    seen.add(key);

    rows.push({
      destination: market,
      year,
      month,
      international: intl ?? 0,
      domestic: dom ?? 0,
    });
  }
  return rows;
}

