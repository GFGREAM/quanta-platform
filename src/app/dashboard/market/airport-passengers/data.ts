/** Auto K/M suffix for passenger counts. No "$" — these are counts, not money. */
export function fmtPax(n: number): string {
  if (!Number.isFinite(n)) return '—';
  const abs = Math.abs(n);
  const sign = n < 0 ? '-' : '';
  if (abs >= 1_000_000) return `${sign}${(abs / 1_000_000).toFixed(2)}M`;
  if (abs >= 1_000) return `${sign}${Math.round(abs / 1_000).toLocaleString('en-US')}K`;
  return `${sign}${Math.round(abs).toLocaleString('en-US')}`;
}

/** Signed percentage like "+8.3%" / "-2.1%". Returns "—" when ref is 0. */
export function fmtVarPct(cur: number, ref: number): string {
  if (!ref || !Number.isFinite(ref)) return '—';
  const pct = ((cur - ref) / ref) * 100;
  const sign = pct >= 0 ? '+' : '';
  return `${sign}${pct.toFixed(1)}%`;
}
